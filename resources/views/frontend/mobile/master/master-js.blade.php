    function loadView(element, type, posisi) {

        var protocol = location.protocol + "//";
        if (protocol == "https://") protocol = "https://";

        switch (type) {
            case "ekspose-mobile": //ekspose mobile
                if (typeof(element.nomer) === "undefined") {
                    $('.ekspose-mobile-ws-' + posisi).css({
                        "display": "none"
                    });
                    break;
                }
                $(".ekspose-mobile-" + element.nomer).css({
                    "position": "absolute",
                    "z-index": "100",
                    "width": "100%"
                });
                $(".ekspose-images-" + element.nomer).append('<a href="%%CLICK_URL_ESC%%' + element.url_ekspose +
                    '" target="_blank"><img loading="lazy" src="' + element.images + '" alt="' + element.title +
                    '" class="responsive-img width100"></a>');
                $(".ekspose-title-" + element.nomer).append('<a href="%%CLICK_URL_ESC%%' + element.url_ekspose +
                    '" target="_blank"><h3 class="ekspose-title">' + element.title + '</h3></a><h5><a>' + element
                    .subtitle + '</a></h5>');
                break;
            case "mobile-bacajuga": //mobile bacajuga

                if (typeof(element.nomer) === "undefined") {
                    break;
                }
                url = protocol + window.location.hostname + "/demo/parallax/" + type;
                $(".mobile-bacajuga-" + element.nomer).css({
                    "position": "absolute",
                    "z-index": "100",
                    "width": "93%"
                });
                $(".mobile-bacajuga-ws-" + element.nomer).append('<div class="posisi-' + element.nomer + '"></div>');
                $(".posisi-" + element.nomer).load(url, function(respones, status, xhr) {
                    if (status == 'success') {
                        $('.mobile-bacajuga-ws-' + element.nomer + ' .bacajuga-images').append(
                            '<img loading="lazy" src="' + element.images + '" alt="' + element.title +
                            '" class="responsive-img width100">');
                        $('.mobile-bacajuga-ws-' + element.nomer + ' .bacajuga-title').append(
                            '<h3 class="bacajuga-title">' + element.title + '</h3><h5><a>' + element
                            .subtitle + '</a></h5>');
                    }
                });
                break;
            case "parallax-desktop": //parallax desktop
                url = protocol + window.location.hostname + "/demo/parallax/" + type; //for live
                $(".parallax_box").css({
                    "width": "100%",
                    "display": "block"
                });
                $(".parallax_desktop").css({
                    "position": "absolute",
                    "z-index": "100",
                    "width": "100%",
                    "display": "block"
                });

                $(".parallax_box").load(url, function(response, status, xhr) {
                    if (status == 'success') {
                        $('.parallax_img_box').append('<img loading="lazy" src="' + element.images +
                            '" class="parallax-img">');
                    }
                });
                break;
            default: //mobile parallax
        }
        return true;
    }
